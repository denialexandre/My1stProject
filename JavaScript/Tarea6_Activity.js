/*Product Class*/
class Product {
    /**
     * @param {string} name The Product Name
     * @param {number} price The Product Price
     * @param {number} description The Product Price
     * @param {number} year The year creation of the Product
     */
    constructor(name, price, description, year) {
      this.name = name;
      this.price = price;
      this.description = description;
      this.year = year;
    }
  }